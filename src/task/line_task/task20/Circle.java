public class Circle {
    public static void main(String[] args) {
        int lengthOrCircle = 10;
        double radiusOfCircle = lengthOrCircle/(2*3.14);
        double areaOfCircle = 3.14 *Math.pow(radiusOfCircle,2);
        System.out.println("Area of circle = " + areaOfCircle);
      }
}
